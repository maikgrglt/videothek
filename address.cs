public class Address{
    private String _street;
    private String _number;
    private String _zip;
    private String _country;
    private String _city;

    public string Street 
    {
        get => _street;
        set => _street = value; 
    }

    public String Number{
        get => _number;
        set => _number = value;
    }

    public String Zip{
        get => _zip;
        set => _zip = value;
    }

    public String Country{
        get => _country;
        set => _country = value;
    }

    public String City{
        get => _city;
        set => _city = value;
    }

}